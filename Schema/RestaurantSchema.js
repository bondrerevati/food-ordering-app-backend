let mongoose = require("mongoose");

const schema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  address: {
    type: String,
    required: true,
  },
  openingTime: {
    type: String,
    required: true,
  },
  closingTime: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
});

const Restaurants = mongoose.model("restaurants", schema);
module.exports = Restaurants;
